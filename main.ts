scene.onHitWall(SpriteKind.Player, function (sprite, location) {
    if (sprite.isHittingTile(CollisionDirection.Bottom)) {
        numberOfJumps = 0
        if (isDangerGround(location.column, location.row)) {
            info.changeLifeBy(-1)
            Jump()
            music.play(music.melodyPlayable(music.smallCrash), music.PlaybackMode.InBackground)
        }
    }
})
scene.onHitWall(SpriteKind.Shuriken, function (sprite, location) {
    if (tiles.tileAtLocationEquals(tiles.getTileLocation(location.column, location.row), sprites.castle.rock2)) {
        tiles.setWallAt(tiles.getTileLocation(location.column, location.row), false)
        tiles.setTileAt(tiles.getTileLocation(location.column, location.row), sprites.builtin.forestTiles10)
        music.play(music.melodyPlayable(music.knock), music.PlaybackMode.InBackground)
    }
})
function clearTouchesEnemies () {
    if (sprites.readDataBoolean(playerSprite, "touchesEnemy")) {
        enemiesTouched = 0
        for (let value of sprites.allOfKind(SpriteKind.Enemy)) {
            if (playerSprite.overlapsWith(value)) {
                enemiesTouched += 1
            }
        }
        if (enemiesTouched == 0) {
            sprites.setDataBoolean(playerSprite, "touchesEnemy", false)
        }
    }
}
